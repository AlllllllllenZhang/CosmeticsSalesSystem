    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:购买
        try {
            Class.forName("org.apache.derby.jdbc.EmbeddedDriver");
        } catch (ClassNotFoundException e) {
            System.out.println("加载驱动程序失败!");
        }

        try {
            String url = "jdbc:derby:化妆品;create=true";  //重要
            Connection con = DriverManager.getConnection(url);
            Statement sql;
            String ql = jTextField2.getText().trim();
            String s = "select * from goods  where gname ='" + ql + "' and gCategory='"+Category.getSelectedItem().toString().trim()+"'";
            sql = con.createStatement();
            ResultSet rs = sql.executeQuery(s);
            if (rs.next()) {
float price = rs.getFloat(3);
                price=price*Float.parseFloat(jTextField1.getText());
                String sPrice = String.valueOf(price);
jLabel3.setText("应付金额："+sPrice);
int n=JOptionPane.showConfirmDialog(null,"您是否要购买"+ql+"商品，总价格为"+price+"元？","提示",JOptionPane.YES_NO_OPTION);
if(n==0)
JOptionPane.showMessageDialog(null,"购买成功！");
else JOptionPane.showMessageDialog(null,"取消购买");

            }

            else
            {
                JOptionPane.showMessageDialog(rootPane, "未找到");
            }
            con.close();
        } catch (SQLException g) {
            System.out.println("E Code" + g.getErrorCode());
            System.out.println("E M" + g.getMessage());
        }

    }//GEN-LAST:event_jButton1MouseClicked

